<p>Escritura fragmentaria, mas no estanca: el pensamiento del desastre arrastra a Blanchot a un diálogo crítico, por momentos con el psicoanálisis (arrumbado por la bella y arrebatadora descripción, autobiográfica, de una «escena primitiva» y su secreto), por momentos con Hegel (ese enemigo inevitable), y sobre todo con Heidegger (el recurso a la etimología siempre en entredicho); pero también, en resonancia creadora con Nietzsche, Kafka, Melville, Hölderlin, Mallarmé, Valéry y René Char.
